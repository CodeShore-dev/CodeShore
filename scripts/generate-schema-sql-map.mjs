// Generate a static { objectName: DDL } map from supabase/schema.sql.
//
// The frontend methodology page shows the real SQL behind each analytical
// number. Rather than parse SQL at runtime, we extract each CREATE VIEW /
// MATERIALIZED VIEW / FUNCTION statement here at build time and emit a typed
// map that the backend serves and the frontend renders.
//
// The splitter is dollar-quote aware: PostgreSQL function bodies are wrapped
// in $tag$ ... $tag$ and contain their own semicolons, so a naive split on
// ';' would slice them apart. We also skip line/block comments and single
// quoted strings while scanning for the top-level statement boundary.
//
// Usage:  pnpm db:sql-map   (or: node scripts/generate-schema-sql-map.mjs)

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import process from 'node:process';

const PROJECT_ROOT = join(
  dirname(fileURLToPath(import.meta.url)),
  '..',
);
const SCHEMA_FILE = join(PROJECT_ROOT, 'supabase/schema.sql');
const OUT_FILE = join(
  PROJECT_ROOT,
  'libs/data-types/src/lib/schema-sql.generated.ts',
);

/**
 * Split a SQL file into top-level statements, respecting dollar-quoted
 * strings, single-quoted strings and comments.
 * @param {string} sql
 * @returns {string[]}
 */
function splitStatements(sql) {
  const statements = [];
  let start = 0;
  let i = 0;
  const n = sql.length;

  while (i < n) {
    const ch = sql[i];
    const next = sql[i + 1];

    // Line comment: -- ... \n
    if (ch === '-' && next === '-') {
      const nl = sql.indexOf('\n', i);
      i = nl === -1 ? n : nl + 1;
      continue;
    }

    // Block comment: /* ... */
    if (ch === '/' && next === '*') {
      const end = sql.indexOf('*/', i + 2);
      i = end === -1 ? n : end + 2;
      continue;
    }

    // Single-quoted string: '...'  ('' is an escaped quote)
    if (ch === "'") {
      i += 1;
      while (i < n) {
        if (sql[i] === "'" && sql[i + 1] === "'") {
          i += 2;
          continue;
        }
        if (sql[i] === "'") {
          i += 1;
          break;
        }
        i += 1;
      }
      continue;
    }

    // Dollar-quoted string: $tag$ ... $tag$
    if (ch === '$') {
      const tagMatch = /^\$[A-Za-z0-9_]*\$/.exec(
        sql.slice(i),
      );
      if (tagMatch) {
        const tag = tagMatch[0];
        const end = sql.indexOf(tag, i + tag.length);
        i = end === -1 ? n : end + tag.length;
        continue;
      }
    }

    // Top-level statement boundary.
    if (ch === ';') {
      statements.push(sql.slice(start, i + 1));
      i += 1;
      start = i;
      continue;
    }

    i += 1;
  }

  // Trailing text without a closing semicolon.
  if (start < n) {
    const tail = sql.slice(start);
    if (tail.trim()) statements.push(tail);
  }

  return statements;
}

const CREATE_VIEW_RE =
  /^CREATE\s+(?:OR\s+REPLACE\s+)?(?:MATERIALIZED\s+VIEW|VIEW)\s+(?:public\.)?"?([A-Za-z0-9_]+)"?/i;
const CREATE_FUNCTION_RE =
  /^CREATE\s+(?:OR\s+REPLACE\s+)?FUNCTION\s+(?:public\.)?"?([A-Za-z0-9_]+)"?\s*\(/i;

/**
 * Drop leading whitespace and comments (e.g. a "-- ==== Views ====" section
 * header that precedes the first CREATE in a section) so the statement starts
 * at its CREATE keyword.
 * @param {string} s
 */
function stripLeading(s) {
  let t = s;
  for (;;) {
    const trimmed = t.replace(/^\s+/, '');
    if (trimmed.startsWith('--')) {
      const nl = trimmed.indexOf('\n');
      t = nl === -1 ? '' : trimmed.slice(nl + 1);
      continue;
    }
    if (trimmed.startsWith('/*')) {
      const end = trimmed.indexOf('*/');
      t = end === -1 ? '' : trimmed.slice(end + 2);
      continue;
    }
    return trimmed;
  }
}

function extractObjects(sql) {
  /** @type {Record<string, string>} */
  const map = {};
  for (const raw of splitStatements(sql)) {
    const ddl = stripLeading(raw);
    if (!ddl) continue;
    const match =
      CREATE_VIEW_RE.exec(ddl) ?? CREATE_FUNCTION_RE.exec(ddl);
    if (!match) continue;
    const name = match[1];
    // First definition wins (schema.sql has one per object).
    if (!(name in map)) map[name] = ddl;
  }
  return map;
}

function main() {
  let sql;
  try {
    sql = readFileSync(SCHEMA_FILE, 'utf8');
  } catch (err) {
    process.stderr.write(
      `Cannot read ${SCHEMA_FILE}: ${err.message}\n`,
    );
    process.exit(1);
  }

  const map = extractObjects(sql);
  const names = Object.keys(map).sort();

  const entries = names
    .map(name => `  ${JSON.stringify(name)}: ${JSON.stringify(map[name])},`)
    .join('\n');

  const out = `// AUTO-GENERATED by scripts/generate-schema-sql-map.mjs. DO NOT EDIT.
// Source: supabase/schema.sql
// Run \`pnpm db:sql-map\` (or \`pnpm db:sync\`) to regenerate.

/**
 * Map of database object name -> its CREATE statement (DDL), extracted from
 * supabase/schema.sql. Covers views, materialized views and functions.
 */
export const SCHEMA_SQL: Record<string, string> = {
${entries}
};
`;

  mkdirSync(dirname(OUT_FILE), { recursive: true });
  writeFileSync(OUT_FILE, out, 'utf8');
  process.stdout.write(
    `Wrote ${names.length} SQL objects to ${OUT_FILE}\n`,
  );
}

main();
