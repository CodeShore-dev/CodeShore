// Sync Supabase schema from the REMOTE project over HTTPS.
// No Docker, no pg_dump, no direct Postgres connection (port 5432/6543 are
// blocked in many environments) -- everything goes through the Supabase
// Management API on https (port 443), the same channel the CLI uses.
//
//   Step 1: introspect the public schema via the Management API and write a
//           schema-only SQL file (tables, sequences, constraints, indexes,
//           views, functions) -- no data.
//   Step 2: run `supabase gen types typescript` and write the TypeScript types.
//
// Requires a Supabase Personal Access Token in SUPABASE_ACCESS_TOKEN
// (create one at https://supabase.com/dashboard/account/tokens and add it to
// the root .env). Step 2's CLI reads the same env var automatically.
//
// Usage:  pnpm db:sync          (or: node scripts/sync-supabase-schema.mjs)

import { execSync } from 'node:child_process';
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import process from 'node:process';

// Auto-load the project-root .env (SUPABASE_ACCESS_TOKEN, etc.).
// Resolved relative to this file (scripts/..), not the current working dir.
const PROJECT_ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
try {
  process.loadEnvFile(join(PROJECT_ROOT, '.env'));
} catch {
  // No .env file -> rely on the existing environment.
}

const PROJECT_ID = 'lsizfvkzpwxkwvohrkmn';
const SCHEMA_FILE = join(PROJECT_ROOT, 'supabase/schema.sql');
const TYPES_FILE = join(PROJECT_ROOT, 'libs/data-types/src/lib/supabase.schema.ts');
const API_BASE = `https://api.supabase.com/v1/projects/${PROJECT_ID}`;

function log(msg) {
  process.stdout.write(`${msg}\n`);
}

const q = (ident) => `"${ident.replace(/"/g, '""')}"`;

// Run a read-only SQL query through the Management API and return the rows.
async function runSql(token, sql) {
  const res = await fetch(`${API_BASE}/database/query`, {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${token}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({ query: sql }),
  });
  if (!res.ok) {
    throw new Error(`Management API ${res.status}: ${await res.text()}`);
  }
  const data = await res.json();
  return Array.isArray(data) ? data : data.result ?? data.rows ?? [];
}

async function dumpSchema(token) {
  const parts = [];
  parts.push('-- Schema-only dump of the public schema (no data).');
  parts.push(`-- Generated by scripts/sync-supabase-schema.mjs on ${new Date().toISOString()}`);
  parts.push('');

  // --- Sequences ---
  const sequences = await runSql(
    token,
    `SELECT c.relname AS name
     FROM pg_class c JOIN pg_namespace n ON n.oid = c.relnamespace
     WHERE n.nspname = 'public' AND c.relkind = 'S'
     ORDER BY c.relname;`
  );
  if (sequences.length) {
    parts.push('-- ============ Sequences ============');
    for (const { name } of sequences) {
      parts.push(`CREATE SEQUENCE IF NOT EXISTS public.${q(name)};`);
    }
    parts.push('');
  }

  // --- Tables + columns (one query, grouped by table in JS) ---
  const columns = await runSql(
    token,
    `SELECT c.relname AS table_name, a.attname AS name,
            pg_catalog.format_type(a.atttypid, a.atttypmod) AS type,
            a.attnotnull AS notnull, a.attidentity AS identity,
            pg_get_expr(d.adbin, d.adrelid) AS default_expr
     FROM pg_class c
     JOIN pg_namespace n ON n.oid = c.relnamespace
     JOIN pg_attribute a ON a.attrelid = c.oid
     LEFT JOIN pg_attrdef d ON d.adrelid = a.attrelid AND d.adnum = a.attnum
     WHERE n.nspname = 'public' AND c.relkind = 'r'
       AND a.attnum > 0 AND NOT a.attisdropped
     ORDER BY c.relname, a.attnum;`
  );

  const byTable = new Map();
  for (const col of columns) {
    if (!byTable.has(col.table_name)) byTable.set(col.table_name, []);
    byTable.get(col.table_name).push(col);
  }

  parts.push('-- ============ Tables ============');
  for (const [table, cols] of byTable) {
    const lines = cols.map((c) => {
      let line = `  ${q(c.name)} ${c.type}`;
      if (c.identity === 'a') line += ' GENERATED ALWAYS AS IDENTITY';
      else if (c.identity === 'd') line += ' GENERATED BY DEFAULT AS IDENTITY';
      else if (c.default_expr) line += ` DEFAULT ${c.default_expr}`;
      if (c.notnull) line += ' NOT NULL';
      return line;
    });
    parts.push(`CREATE TABLE public.${q(table)} (\n${lines.join(',\n')}\n);`);
  }
  parts.push('');

  // --- Constraints (PK, UNIQUE, FK, CHECK) ---
  const constraints = await runSql(
    token,
    `SELECT t.relname AS table_name, con.conname AS name,
            pg_get_constraintdef(con.oid) AS def
     FROM pg_constraint con
     JOIN pg_class t ON t.oid = con.conrelid
     JOIN pg_namespace n ON n.oid = t.relnamespace
     WHERE n.nspname = 'public'
     ORDER BY t.relname, (con.contype = 'f'), con.conname;`
  );
  if (constraints.length) {
    parts.push('-- ============ Constraints ============');
    for (const c of constraints) {
      parts.push(
        `ALTER TABLE public.${q(c.table_name)} ADD CONSTRAINT ${q(c.name)} ${c.def};`
      );
    }
    parts.push('');
  }

  // --- Indexes (excluding those backing a constraint) ---
  const indexes = await runSql(
    token,
    `SELECT i.indexdef AS def
     FROM pg_indexes i
     WHERE i.schemaname = 'public'
       AND NOT EXISTS (
         SELECT 1 FROM pg_constraint con
         WHERE con.conname = i.indexname
           AND con.connamespace = 'public'::regnamespace
       )
     ORDER BY i.tablename, i.indexname;`
  );
  if (indexes.length) {
    parts.push('-- ============ Indexes ============');
    for (const i of indexes) parts.push(`${i.def};`);
    parts.push('');
  }

  // --- Views & materialized views ---
  const views = await runSql(
    token,
    `SELECT c.relname AS name, c.relkind AS kind, pg_get_viewdef(c.oid, true) AS def
     FROM pg_class c JOIN pg_namespace n ON n.oid = c.relnamespace
     WHERE n.nspname = 'public' AND c.relkind IN ('v', 'm')
     ORDER BY c.relkind, c.relname;`
  );
  if (views.length) {
    parts.push('-- ============ Views ============');
    for (const v of views) {
      const keyword =
        v.kind === 'm' ? 'CREATE MATERIALIZED VIEW' : 'CREATE OR REPLACE VIEW';
      parts.push(`${keyword} public.${q(v.name)} AS\n${v.def}`);
      parts.push('');
    }
  }

  // --- Functions & procedures (excluding extension-owned) ---
  const functions = await runSql(
    token,
    `SELECT pg_get_functiondef(p.oid) AS def
     FROM pg_proc p JOIN pg_namespace n ON n.oid = p.pronamespace
     WHERE n.nspname = 'public' AND p.prokind IN ('f', 'p')
       AND NOT EXISTS (
         SELECT 1 FROM pg_depend d WHERE d.objid = p.oid AND d.deptype = 'e'
       )
     ORDER BY p.proname;`
  );
  if (functions.length) {
    parts.push('-- ============ Functions ============');
    for (const f of functions) {
      const def = f.def.trimEnd();
      parts.push(def.endsWith(';') ? def : `${def};`);
      parts.push('');
    }
  }

  return parts.join('\n');
}

async function main() {
  const token = process.env.SUPABASE_ACCESS_TOKEN;
  if (!token) {
    log('SUPABASE_ACCESS_TOKEN is not set.');
    log('Create a personal access token at https://supabase.com/dashboard/account/tokens');
    log('then add it to the root .env as:  SUPABASE_ACCESS_TOKEN=sbp_xxx');
    process.exit(1);
  }

  log('--- Step 1: Dumping remote schema (tables, views, indexes, functions; no data) ---');
  try {
    const sql = await dumpSchema(token);
    mkdirSync(dirname(SCHEMA_FILE), { recursive: true });
    writeFileSync(SCHEMA_FILE, sql, 'utf8');
    log(`Schema written to ${SCHEMA_FILE}`);
  } catch (err) {
    log(`Schema dump failed: ${err.message}`);
    process.exit(1);
  }

  log('--- Step 2: Generating TypeScript types ---');
  try {
    mkdirSync(dirname(TYPES_FILE), { recursive: true });
    // The CLI reads SUPABASE_ACCESS_TOKEN from the environment (already loaded).
    const rawTypes = execSync(
      `pnpm supabase gen types typescript --project-id ${PROJECT_ID} --schema public`,
      { encoding: 'utf8', maxBuffer: 64 * 1024 * 1024, cwd: PROJECT_ROOT }
    );
    // pnpm sometimes prints an "Unsupported engine" warning to stdout ahead of
    // the actual command output (observed when the local Node version doesn't
    // match package.json's engines.node), which would otherwise get baked
    // into the generated file as invalid TypeScript. The real output always
    // starts with the `Json` type alias, so discard anything before that.
    const markerIndex = rawTypes.indexOf('export type Json');
    if (markerIndex === -1) {
      throw new Error(
        'Could not find "export type Json" in `supabase gen types typescript` output -- CLI output format may have changed or the command failed silently.'
      );
    }
    const types = rawTypes.slice(markerIndex);
    writeFileSync(TYPES_FILE, types, 'utf8');
    log(`Types written to ${TYPES_FILE}`);
  } catch (err) {
    log(`Type generation failed: ${err.message}`);
    process.exit(1);
  }

  log('Done!');
}

main();
